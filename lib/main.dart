import 'package:flutter/material.dart';
import 'package:firebase_core/firebase_core.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:order2/app/bloc/authentication_bloc.dart'; // Ensure you import your bloc
import 'package:order2/screens/authentication_page.dart';
import 'firebase_options.dart';

void main() async {
  WidgetsFlutterBinding.ensureInitialized();
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );
  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    return BlocProvider(
      create: (context) => AuthenticationBloc(), // Provide the AuthenticationBloc
      child: const MaterialApp(
        home: AuthenticationFlowScreen(),
      ),
    );
  }
}
